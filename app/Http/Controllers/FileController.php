    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $files = File::with(['files'])->doesntHave('file')->get();

        return view('admin.files.index', ['files' => $files]);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $collections = Collection::with(['files', 'imageSpecs'])->get();

        return view('admin.files.create', ['collections' => $collections]);
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \App\Http\Requests\StoreFileRequest  $request
     * @return \Illuminate\Http\Response
     */
    public function store(StoreFileRequest $request)
    {
        FileService::newFiles($request->file('files'), $request->data);

        return redirect()->route('admin.files.index');
    }

